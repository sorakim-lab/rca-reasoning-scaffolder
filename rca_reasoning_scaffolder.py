    "        print(f\"Active hypotheses: {check['active_hypotheses']}\")\n",
    "        print(f\"Premature closure warning: {check['warning']}\")\n",
    "        print(f\"Message: {check['message']}\")\n",
    "        print(\"=\" * 72 + \"\\n\")\n",
    "\n",
    "    def build_graph(self) -> nx.DiGraph:\n",
    "        G = nx.DiGraph()\n",
    "\n",
    "        issue_node = f\"Issue: {self.rca.issue}\"\n",
    "        G.add_node(issue_node, node_type=\"issue\")\n",
    "\n",
    "        for h in self.rca.hypotheses:\n",
    "            h_node = f\"{h.id}: {h.description}\"\n",
