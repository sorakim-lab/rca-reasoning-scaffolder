    "from __future__ import annotations\n",
    "\n",
    "from dataclasses import dataclass, field\n",
    "from typing import List, Optional, Dict\n",
    "import pandas as pd\n",
    "import networkx as nx\n",
    "import matplotlib.pyplot as plt\n",
    "\n",
    "\n",
    "# =========================\n",
    "# Data structures\n",
    "# =========================\n",
    "\n",
    "@dataclass\n",
    "class Factor:\n",
    "    id: str\n",
    "    description: str\n",
    "\n",
    "\n",
    "@dataclass\n",
    "class Evidence:\n",
    "    id: str\n",
    "    description: str\n",
    "\n",
    "\n",
    "@dataclass\n",
    "class Hypothesis:\n",
    "    id: str\n",
    "    description: str\n",
    "    status: str = \"active\"   # active / narrowed / closed / rejected\n",
    "    factors: List[Factor] = field(default_factory=list)\n",
    "    evidence: List[Evidence] = field(default_factory=list)\n",
    "    notes: List[str] = field(default_factory=list)\n",
    "\n",
    "\n",
    "@dataclass\n",
    "class RCARecord:\n",
    "    issue: str\n",
    "    hypotheses: List[Hypothesis] = field(default_factory=list)\n",
    "    final_closure: Optional[str] = None\n",
    "\n",
    "\n",
    "# =========================\n",
    "# RCA scaffold logic\n",
    "# =========================\n",
    "\n",
    "class RCAReasoningScaffolder:\n",
    "    def __init__(self, issue: str):\n",
    "        self.rca = RCARecord(issue=issue)\n",
    "\n",
    "    def _next_hypothesis_id(self) -> str:\n",
    "        return f\"H{len(self.rca.hypotheses) + 1}\"\n",
    "\n",
    "    def _next_factor_id(self, hypothesis: Hypothesis) -> str:\n",
    "        return f\"F{len(hypothesis.factors) + 1}\"\n",
    "\n",
    "    def _next_evidence_id(self, hypothesis: Hypothesis) -> str:\n",
    "        return f\"E{len(hypothesis.evidence) + 1}\"\n",
    "\n",
    "    def _find_hypothesis(self, hypothesis_id: str) -> Hypothesis:\n",
    "        for h in self.rca.hypotheses:\n",
    "            if h.id == hypothesis_id:\n",
    "                return h\n",
    "        raise ValueError(f\"Hypothesis '{hypothesis_id}' not found.\")\n",
    "\n",
    "    def add_hypothesis(self, description: str, status: str = \"active\") -> str:\n",
    "        new_id = self._next_hypothesis_id()\n",
    "        self.rca.hypotheses.append(\n",
    "            Hypothesis(\n",
    "                id=new_id,\n",
    "                description=description,\n",
    "                status=status\n",
    "            )\n",
    "        )\n",
    "        return new_id\n",
    "\n",
    "    def add_factor(self, hypothesis_id: str, description: str) -> str:\n",
    "        h = self._find_hypothesis(hypothesis_id)\n",
    "        new_id = self._next_factor_id(h)\n",
    "        h.factors.append(Factor(id=new_id, description=description))\n",
    "        return new_id\n",
    "\n",
    "    def add_evidence(self, hypothesis_id: str, description: str) -> str:\n",
    "        h = self._find_hypothesis(hypothesis_id)\n",
    "        new_id = self._next_evidence_id(h)\n",
    "        h.evidence.append(Evidence(id=new_id, description=description))\n",
    "        return new_id\n",
    "\n",
    "    def add_note(self, hypothesis_id: str, note: str) -> None:\n",
    "        h = self._find_hypothesis(hypothesis_id)\n",
    "        h.notes.append(note)\n",
    "\n",
    "    def update_status(self, hypothesis_id: str, new_status: str) -> None:\n",
    "        allowed = {\"active\", \"narrowed\", \"closed\", \"rejected\"}\n",
    "        if new_status not in allowed:\n",
    "            raise ValueError(f\"Invalid status '{new_status}'. Allowed: {allowed}\")\n",
    "        h = self._find_hypothesis(hypothesis_id)\n",
    "        h.status = new_status\n",
    "\n",
    "    def set_final_closure(self, hypothesis_id: str) -> None:\n",
    "        h = self._find_hypothesis(hypothesis_id)\n",
    "        self.rca.final_closure = h.id\n",
    "        h.status = \"closed\"\n",
    "\n",
    "    def get_active_hypotheses(self) -> List[Hypothesis]:\n",
    "        return [h for h in self.rca.hypotheses if h.status in {\"active\", \"narrowed\"}]\n",
    "\n",
    "    def check_premature_closure(self) -> Dict[str, object]:\n",
    "        active = self.get_active_hypotheses()\n",
    "\n",
    "        warning = False\n",
    "        message = \"Reasoning remains open enough.\"\n",
    "\n",
    "        if len(active) <= 1 and self.rca.final_closure is None:\n",
    "            warning = True\n",
    "            message = (\n",
    "                \"Warning: reasoning may be prematurely narrowed. \"\n",
    "                \"Only one active hypothesis remains before formal closure.\"\n",
    "            )\n",
    "\n",
    "        if len(self.rca.hypotheses) == 1:\n",
    "            warning = True\n",
    "            message = (\n",
    "                \"Warning: only one hypothesis was considered. \"\n",
    "                \"Alternative explanations may not have been preserved.\"\n",
    "            )\n",
    "\n",
    "        return {\n",
    "            \"total_hypotheses\": len(self.rca.hypotheses),\n",
    "            \"active_hypotheses\": len(active),\n",
    "            \"warning\": warning,\n",
    "            \"message\": message\n",
    "        }\n",
    "\n",
    "    def summary_table(self) -> pd.DataFrame:\n",
    "        rows = []\n",
    "\n",
    "        for h in self.rca.hypotheses:\n",
    "            if not h.factors and not h.evidence:\n",
    "                rows.append({\n",
    "                    \"Hypothesis ID\": h.id,\n",
    "                    \"Hypothesis\": h.description,\n",
    "                    \"Status\": h.status,\n",
    "                    \"Factor ID\": \"\",\n",
    "                    \"Factor\": \"\",\n",
    "                    \"Evidence ID\": \"\",\n",
    "                    \"Evidence\": \"\",\n",
    "                    \"Notes Count\": len(h.notes)\n",
    "                })\n",
    "                continue\n",
    "\n",
    "            max_len = max(len(h.factors), len(h.evidence), 1)\n",
    "\n",
    "            for i in range(max_len):\n",
    "                factor = h.factors[i] if i < len(h.factors) else None\n",
    "                evidence = h.evidence[i] if i < len(h.evidence) else None\n",
    "\n",
    "                rows.append({\n",
    "                    \"Hypothesis ID\": h.id,\n",
    "                    \"Hypothesis\": h.description,\n",
    "                    \"Status\": h.status,\n",
    "                    \"Factor ID\": factor.id if factor else \"\",\n",
    "                    \"Factor\": factor.description if factor else \"\",\n",
    "                    \"Evidence ID\": evidence.id if evidence else \"\",\n",
    "                    \"Evidence\": evidence.description if evidence else \"\",\n",
    "                    \"Notes Count\": len(h.notes)\n",
    "                })\n",
    "\n",
    "        return pd.DataFrame(rows)\n",
    "\n",
    "    def print_summary(self) -> None:\n",
    "        print(\"\\n\" + \"=\" * 72)\n",
    "        print(\"RCA REASONING SCAFFOLDER\")\n",
    "        print(\"=\" * 72)\n",
    "        print(f\"Issue: {self.rca.issue}\")\n",
    "        print(f\"Final closure: {self.rca.final_closure if self.rca.final_closure else 'Not yet closed'}\")\n",
