    "            if h.evidence:\n",
    "                for e in h.evidence:\n",
    "                    print(f\"   - Evidence [{e.id}] {e.description}\")\n",
    "            else:\n",
    "                print(\"   - Evidence [none]\")\n",
    "\n",
    "            if h.notes:\n",
    "                for n in h.notes:\n",
