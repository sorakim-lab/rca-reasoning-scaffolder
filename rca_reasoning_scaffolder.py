    "            G.add_edge(issue_node, h_node, edge_type=\"hypothesis\")\n",
    "\n",
    "            for f in h.factors:\n",
    "                f_node = f\"{f.id}: {f.description}\"\n",
    "                G.add_node(f_node, node_type=\"factor\")\n",
    "                G.add_edge(h_node, f_node, edge_type=\"factor\")\n",
    "\n",
    "            for e in h.evidence:\n",
    "                e_node = f\"{e.id}: {e.description}\"\n",
    "                G.add_node(e_node, node_type=\"evidence\")\n",
    "                G.add_edge(h_node, e_node, edge_type=\"evidence\")\n",
    "\n",
    "        return G\n",
    "\n",
    "    def plot_graph(self, save_path: str = \"rca_reasoning_graph.png\") -> None:\n",
    "        G = self.build_graph()\n",
    "        pos = nx.spring_layout(G, seed=42, k=1.0)\n",
    "\n",
    "        node_colors = []\n",
    "        for _, data in G.nodes(data=True):\n",
    "            node_type = data.get(\"node_type\")\n",
    "\n",
    "            if node_type == \"issue\":\n",
    "                node_colors.append(\"lightgray\")\n",
    "            elif node_type == \"hypothesis\":\n",
    "                status = data.get(\"status\", \"active\")\n",
    "                if status == \"closed\":\n",
    "                    node_colors.append(\"lightcoral\")\n",
    "                elif status == \"rejected\":\n",
    "                    node_colors.append(\"gainsboro\")\n",
    "                elif status == \"narrowed\":\n",
    "                    node_colors.append(\"khaki\")\n",
    "                else:\n",
    "                    node_colors.append(\"lightskyblue\")\n",
    "            elif node_type == \"factor\":\n",
    "                node_colors.append(\"lightgreen\")\n",
    "            elif node_type == \"evidence\":\n",
    "                node_colors.append(\"plum\")\n",
    "            else:\n",
    "                node_colors.append(\"white\")\n",
    "\n",
    "        plt.figure(figsize=(14, 9))\n",
    "        nx.draw_networkx_nodes(G, pos, node_color=node_colors, node_size=2400, edgecolors=\"black\")\n",
    "        nx.draw_networkx_edges(G, pos, arrows=True, arrowstyle=\"->\", arrowsize=16, width=1.5)\n",
    "        nx.draw_networkx_labels(G, pos, font_size=8)\n",
    "\n",
    "        plt.title(\"RCA Reasoning Scaffold\", fontsize=14)\n",
    "        plt.axis(\"off\")\n",
    "        plt.tight_layout()\n",
    "        plt.savefig(save_path, dpi=200)\n",
    "        plt.close()\n",
    "\n",
